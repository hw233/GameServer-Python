def rewardHelpPoint(taskObj, who):
	#########################################################################
	#  0     1     2		3	    4										#
	#[(LEVEL,POWER,ISLEADER,RANDOM,ID), (LEVEL,POWER,ISLEADER,RANDOM,ID)]	#
	#  0				  				 1  								#
	#########################################################################
	ilpoList = getIdLevelPowerDict(who)
	ilpoListInva = ilpoList[:]
	if not ilpoList:
		return 
	for ilpoEleflag in ilpoListInva:
		bVal = 0
		for ilpoEleOther in ilpoListInva:
			if ilpoEleflag[0] >= (ilpoEleOther[0]+10):
				bVal += 1
		if not bVal:
			ilpoList.remove(ilpoEleflag)
		else:
			if bVal > 3:
				bVal = 3
			ilpoEleflag[3] = bVal

	if not ilpoList:
		return
	byRoleIdAddHelpPoint(taskObj, ilpoList)

def onNewRoundForEscape(w):
	'''回合前设置逃跑指令
	'''
	warObj = w.war
	side = w.side ^ 1
	isEscape = False
	for _w in warObj.teamList[side].values():
		if not _w.isRole():
			continue
		who = getRole(_w.getPID())
		if not who:
			continue
		if who.school not in (12, 14,16,):
			continue
		isEscape = True
		break
	if isEscape:
		war.commands.setCommand(warObj, w, CMD_TYPE_ESCAPE)
		w.addFunc("onCommand", onCommandForEscape)

