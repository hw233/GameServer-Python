def getIdLevelPowerDict(who):
	teamObj = who.getTeamObj()
	if not teamObj:
		return
	teamList = teamObj.getMemberList()
	ilpoList = []
	for pid in teamList:
		roleObj = getRole(pid)
		if not roleObj:
			continue
		val = 0		#默认0占个位
		lev = roleObj.level
		gra = grade.gradeAll(roleObj)
		lea = teamObj.isLeader(pid)
		buf = [lev, gra, lea, val, pid]
		ilpoList.append(buf)
	ilpoList.sort(None, None, True)
	return ilpoList

