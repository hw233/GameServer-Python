	def updateRewardVal(self, who, val):
		'''更新奖励值
		'''
		# 队长在每次战斗后，队长奖励系数都比队员高，如队员为1，队长为1.2，即所有元宝、银币奖励X1.2，同时物品奖励几率也提高1.2
		teamObj = who.getTeamObj()
		if teamObj and teamObj.isLeader(who.id):
			val *= 1.2
		if task.offlineTask.inOfflineTask(who):
			return int(val)
		bDouble = True if who.doublePoint >= 1 else False
		val = val * 8.5 if bDouble else val#目前策划要求8.5倍
		return int(val)

