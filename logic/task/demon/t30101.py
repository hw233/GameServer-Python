	def createRandPos(self,who):
		# 获取sceneId的途径
		teamObj = who.getTeamObj()
		ring = who.fetch("demonRing")
		limo = self.getLimoCount()
		tianmo = self.getTianmoCount()
		teamAvgLV = teamObj.getAvgLV()
		mapData = None
		for key in sorted(self.mapInfo.keys(),reverse=True):
			if teamAvgLV >= key:
				mapData = self.mapInfo.get(key)
				break
		if not mapData:
			raise PlannerError('没有小于{}队伍等级的地图分配'.format(teamAvgLV))
		
		mapList = []
		if ring % limo == 0:#厉魔地图
			mapList = mapData["厉魔地图"]
		elif ring % tianmo == 0:#天魔地图
			mapList = mapData["天魔地图"]
		else:#普通地图
			mapList = mapData["降魔地图"]
		sceneId = mapList[rand(len(mapList))]
		x, y = scene.randSpace(sceneId)
		return "%d,%d,%d,0" % (sceneId, x, y)

