			limo = self.getLimoCount()
			tianmo = self.getTianmoCount()
			for obj in self.getRoleList():
				if task.offlineTask.inOfflineTask(obj): # 离线任务中
					task.offlineTask.addOfflineRing(obj)
				else:
					dr = obj.day.add("demonRing", 1)
					if dr <= 20:
						perPoint = activity.center.getPerActPoint(1)
						obj.addActPoint(perPoint)
