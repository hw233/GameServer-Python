	def rewardByType(self, who, val, _type):
		if _type == "侠义值":
			self.rewardHelpPoint(who, val, _type)
			return
		template.Template.rewardByType(self, who, val, _type)
		

	def rewardHelpPoint(self, who, val, _type):
		teamObj = who.getTeamObj()
		if not teamObj:
			return
		teamList = teamObj.getInTeamList()
		for pid in teamList:
			if pid == who.id:
				continue
			target = getRole(pid)
			if target:
				target.addHelpPoint(val, self.name)

		
