	def validDoEventScript(self, who, npcObj, key):
		needLevel = getattr(self,"needLevel",0)
		if who.level<needLevel:
			message.tips(who,"请先升到#C04{}级#n".format(needLevel))
			return 0
		return customTask.validDoEventScript(self, who, npcObj, key)

	def customEvent(self, who, npcObj, eventName, *args):
		m = re.match("next(\d+)", eventName)
		if m:
			nextTaskId = int(m.group(1))
			if task.hasTask(who,40001):
				return
			if who.level<nextTaskId%10000/10:
				nextTaskId = 40000

			task.newTask(who, npcObj, nextTaskId)
	
