	def customEvent(self, who, npcObj, eventName, *args):
		if eventName.startswith("next"):
			m = re.match("next(\d+)", eventName)
			if not m:
				return
			nextTaskId = int(m.group(1))
			if task.hasTask(who,40001):
				return
			if who.level<nextTaskId%10000/100*10-20:
				nextTaskId = 40000

			task.newTask(who, npcObj, nextTaskId)

		elif eventName == "fight":
			fightIdx = args[0]
			eventIdx = args[1]
			npcIdx = args[2]
			npcObj = self.addTempNpc(npcIdx, who)
			npcObj.eventIdx = eventIdx
			self.fight(who, npcObj, fightIdx)

		elif eventName.startswith("toast"):
			m = re.match("toast(\d+)", eventName)
			if not m:
				return
			toast = m.group(1)
			message.tips(who,toast)

		elif eventName.startswith("buddy"):
			m = re.match("buddy(\d+)", eventName)
			if not m:
				return
			iBuddyNo = int(m.group(1))
			if not who.buddyCtn.getItem(iBuddyNo):
				buddyObj = buddy.add(who,iBuddyNo)
				message.tips(who,"成功召唤#C02%s#n" % buddyObj.name)
				
	def rewardByType(self, who, val, _type):
		if _type == "侠义值":
			teamObj = who.getTeamObj()
			if not teamObj:
				return
			teamList = teamObj.getInTeamList()
			for pid in teamList:
				if pid == who.id:
					continue
				self.byIdGetHelpPoint(pid, val)
			return
		template.Template.rewardByType(self, who, val, _type)

	def byIdGetHelpPoint(self, pid, val):
		target = getRole(pid)
		if not target:
			return
		if target.level < 40:
			return
		if target:
			target.addHelpPoint(val, self.name)
			
